export const groupBy = <T>(list: T[], keyGetter: (r: T) => string | number | boolean | Date) => {
    const map = new Map<string | number | boolean | Date, T[]>();
    list.forEach((item: T) => {
        const key = keyGetter(item);
        const collection = map.get(key);
        if (!collection) {
            map.set(key, [item]);
        } else {
            collection.push(item);
        }
    });
    return map;
}